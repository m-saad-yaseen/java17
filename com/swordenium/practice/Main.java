package com.swordenium.practice;

public class Main {
    public static void main(String[] args) {
        var number = 10;
        var word = "String";

        /*
        It allows programmers to declare the type of the local variable instead of the actual type
         and increases code readability.
         The compiler decides the type based on the value assigned to the variable.
        * **/
        System.out.println(number);
        System.out.println(word);
    }
}
